logger = logging.getLogger(__name__)


class AchievementType(Enum):
    """Types of achievements."""
    EXPLORER = "explorer"
    CULTURAL = "cultural"
    LANGUAGE = "language"
    SOCIAL = "social"
    MILESTONE = "milestone"
    SPECIAL = "special"


class ChallengeType(Enum):
    """Types of cultural challenges."""
    QUIZ = "quiz"
    PHOTO = "photo"
    VISIT = "visit"
    LEARNING = "learning"
    SOCIAL = "social"
    CREATIVE = "creative"


class TourGuideType(Enum):
    """Types of virtual tour guides."""
    HISTORIAN = "historian"
    CULTURAL_EXPERT = "cultural_expert"
    NATURE_GUIDE = "nature_guide"
    FOOD_CRITIC = "food_critic"
    ADVENTURE_GUIDE = "adventure_guide"


@dataclass
class Achievement:
    """Achievement structure."""
    id: str
    name: str
    name_si: str
    name_ta: str
    description: str
    description_si: str
    description_ta: str
    type: AchievementType
    points: int
    icon: str
    rarity: str  # common, rare, epic, legendary
    requirements: Dict[str, Any]
    unlocked_at: Optional[datetime] = None


@dataclass
class Challenge:
    """Cultural challenge structure."""
    id: str
    name: str
    name_si: str
    name_ta: str
    description: str
    description_si: str
    description_ta: str
    type: ChallengeType
    difficulty: str  # easy, medium, hard, expert
    points: int
    duration: Optional[timedelta] = None
    requirements: Dict[str, Any]
    rewards: List[str]
    is_active: bool = True


@dataclass
class VirtualTourGuide:
    """Virtual tour guide structure."""
    id: str
    name: str
    name_si: str
    name_ta: str
    type: TourGuideType
    personality: str
    expertise: List[str]
    greeting_message: str
    greeting_message_si: str
    greeting_message_ta: str
    avatar_url: Optional[str] = None
    is_available: bool = True


@dataclass
class UserProgress:
    """User progress and achievements."""
    user_id: str
    level: int
    experience_points: int
    total_points: int
    achievements: List[str]
    active_challenges: List[str]
    completed_challenges: List[str]
    current_guide: Optional[str] = None
    guide_history: List[str]
    created_at: datetime
    last_updated: datetime


